# 1. Define two integers a and b
a = 6
b = 4

# 2. Perform and print the results of various operations
print("Sum of a and b:", a + b)             # Sum
print("Difference of a and b:", a - b)      # Difference
print("Product of a and b:", a * b)         # Product
print("Integer quotient of a and b:", a // b)  # Integer division
print("Remainder of a divided by b:", a % b)   # Remainder
print("a raised to the power of b:", a ** b)   # Exponentiation
